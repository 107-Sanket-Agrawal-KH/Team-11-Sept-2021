
//15. Write a Java program to swap two variables
import java.util.*;
class swap
{
	public static void main(String args[])
	{
		int num1 = 3; 
		int num2 = 2;
		System.out.println("num1=" +num1 + "num2=" +num2);
		int temp;
		temp = num1;
		num1 = num2;
		num2 = temp;
		System.out.println("num1=" +num1 + "num2=" +num2);
	}
}
		
		
